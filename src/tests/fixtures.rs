/// Synthetic sysex fixture: simple pattern.
/// All C notes, Normal transpose, no accent/slide/tie/rest, 16 active steps, triplet off.
///
/// Layout (115 bytes):
///   [0x00]       0x78  message ID
///   [0x01-0x02]  patgroup=0, pattern=0
///   [0x03-0x04]  unknown1: 00, 01
///   [0x05-0x24]  notes: 16 steps x 2 bytes each
///                C Normal = MIDI note 24 (0x18) -> high=0x01, low=0x08
///   [0x25-0x44]  accent: all 0
///   [0x45-0x64]  slide: all 0
///   [0x65-0x66]  triplet: 00, 00
///   [0x67-0x68]  active_steps=16: 0x01, 0x00
///   [0x69-0x6A]  unknown2: 00, 00
///   [0x6B-0x6E]  ties: 0x0F x4 (all Normal)
///   [0x6F-0x72]  rests: 00, 00, 00, 00
pub fn simple_sysex() -> Vec<u8> {
    let mut msg = vec![0u8; 115];
    msg[0x00] = 0x78; // message ID
    msg[0x01] = 0x00; // patgroup
    msg[0x02] = 0x00; // pattern
    msg[0x03] = 0x00; // unknown1
    msg[0x04] = 0x01; // unknown1

    // C at Normal transpose = MIDI note 24 = 0x18
    // high nibble byte = 0x01, low nibble byte = 0x08
    for n in 0..16 {
        let dn = n * 2;
        msg[0x05 + dn] = 0x01; // high nibble of note
        msg[0x06 + dn] = 0x08; // low nibble of note
    }

    // accent, slide: already 0 from vec initialization

    // triplet off
    msg[0x65] = 0x00;
    msg[0x66] = 0x00;

    // active_steps = 16: high nibble = 1, low nibble = 0
    msg[0x67] = 0x01;
    msg[0x68] = 0x00;

    // unknown2
    msg[0x69] = 0x00;
    msg[0x6A] = 0x00;

    // Tie flags: In the TD-3 protocol, Time::Normal = 0b01, meaning the tie bit is SET.
    // For all 16 steps to be Normal, tie u16 = 0xFFFF.
    // Nibble encoding: four_u8_to_u16 reconstructs as (b0<<4)+b1+(b2<<12)+(b3<<8)
    // For 0xFFFF: all four nibble bytes = 0x0F.
    msg[0x6B] = 0x0F;
    msg[0x6C] = 0x0F;
    msg[0x6D] = 0x0F;
    msg[0x6E] = 0x0F;

    // Rest flags: all 0 for Normal time
    // (already 0 from initialization)

    msg
}

